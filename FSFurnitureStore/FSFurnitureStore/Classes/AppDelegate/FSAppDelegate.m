- (UINavigationController *)currentNav {
    
    UITabBarController *tabBar = (FSBaseTabBarViewController *)[UIApplication sharedApplication].keyWindow.rootViewController;
    
    if (![tabBar isKindOfClass:[UITabBarController class]]) return nil;
    
    UINavigationController *nav = (FSBaseNavigationController *)tabBar.selectedViewController;
    
    return nav;
}

